// ─── Core i18n engine ────────────────────────────────────────────────────────

let currentLang = localStorage.getItem('elgasco_lang') || 'de';

function t(key) {
  return T[currentLang][key] || T['de'][key] || key;
}

function applyTranslations() {
  const lang = currentLang;

  // Text nodes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    el.textContent = t(key);
  });

  // HTML content
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    el.innerHTML = t(key);
  });

  // Placeholders
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPh);
  });

  // Update lang switcher button
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.textContent = lang === 'de' ? 'EN' : 'DE';
    btn.title = lang === 'de' ? 'Switch to English' : 'Zur deutschen Version';
  });

  // Update <html> lang attribute
  document.documentElement.lang = lang;

  // Notify catalog if it exists
  if (typeof onLangChange === 'function') onLangChange(lang);
}

function toggleLang() {
  currentLang = currentLang === 'de' ? 'en' : 'de';
  localStorage.setItem('elgasco_lang', currentLang);
  applyTranslations();
}

document.addEventListener('DOMContentLoaded', applyTranslations);

